/// Defines the direction in which the shimmer effect moves.
/// 
/// This is used to control the animation direction of the shimmer effect.
enum ShimmerDirection {
  /// Shimmer moves from left to right.
  leftToRight,

  /// Shimmer moves from top to bottom.
  topToBottom,

  /// Shimmer moves diagonally from top-left to bottom-right.
  diagonal,
}